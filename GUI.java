    private void queryWaitlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_queryWaitlistActionPerformed
        // TODO add your handling code here:
        waitlistTextArea.setText(null);
        results = dataQueries.getWaitByDate(comboDate.getSelectedItem().toString());
        numberOfEntries = results.size();
            waitlistTextArea.append("Name"+"\t\t"+"Date"+"\t\t"+"Fligh"+"\t\t"+"Status"+"\n");
            //waitlistTextArea
        for(int i =0; i< numberOfEntries; i++){
            waitlistTextArea.append((results.get(i)).getName()+"\t\t");            
            waitlistTextArea.append((results.get(i)).getDate()+"\t\t");
            waitlistTextArea.append((results.get(i)).getFlight()+"\t\t");
            waitlistTextArea.append((results.get(i)).getStatus()+"\t\t"+"\n");           
        }
        waitlistTextArea.setEditable(false);
        
    }//GEN-LAST:event_queryWaitlistActionPerformed

