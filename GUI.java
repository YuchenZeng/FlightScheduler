    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        // TODO add your handling code here:
        int result = dataQueries.addCustomer(addTextField.getText());
        if(result ==1){
        JOptionPane.showMessageDialog(this,"Person added!","Person added",JOptionPane.PLAIN_MESSAGE);   
        String[] names = dataQueries.getName().toArray(new String[dataQueries.getName().size()]);
        comboName.setModel(new javax.swing.DefaultComboBoxModel<>(names));
        String[] dates = dataQueries.getDate().toArray(new String[dataQueries.getDate().size()]);
        comboDate.setModel(new javax.swing.DefaultComboBoxModel<>(dates));
        addTextField.setText(null);
        }
        else
        JOptionPane.showMessageDialog(this,"Person not added!","Error",JOptionPane.PLAIN_MESSAGE);
        browseButtonActionPerformed(evt);
    }//GEN-LAST:event_insertButtonActionPerformed

    private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed
        // TODO add your handling code here:
        try{
            results = dataQueries.getAllPeople();
            numberOfEntries = results.size();
            if(numberOfEntries != 0){
                currentEntryIndex = 0;
                currentEntry =results.get(currentEntryIndex);
                idTextField.setText(""+currentEntry.getName());
                dateTextField.setText(""+currentEntry.getDate());
                statusTextField.setText(""+currentEntry.getStatus());
                flightTextField.setText(""+currentEntry.getFlight());
                maxTextField.setText(""+numberOfEntries);
                indexTextField.setText(""+(currentEntryIndex+1));
                nextButton.setEnabled(true);
                previousButton.setEnabled(true);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_browseButtonActionPerformed

    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        // TODO add your handling code here:
        currentEntryIndex++;
        if(currentEntryIndex >= numberOfEntries)
        currentEntryIndex = 0;
        indexTextField.setText(""+(currentEntryIndex + 1));
        indexTextFieldActionPerformed(evt);
    }//GEN-LAST:event_nextButtonActionPerformed

    private void indexTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_indexTextFieldActionPerformed
        // TODO add your handling code here:
        currentEntryIndex = (Integer.parseInt(indexTextField.getText())-1);
        if(numberOfEntries != 0 && currentEntryIndex <numberOfEntries){
            currentEntry = results.get(currentEntryIndex);
            idTextField.setText(""+currentEntry.getName());
            dateTextField.setText(""+currentEntry.getDate());
            statusTextField.setText(""+currentEntry.getStatus());
            flightTextField.setText(""+currentEntry.getFlight());
            maxTextField.setText(""+numberOfEntries);
            indexTextField.setText(""+(currentEntryIndex+1));
        }
    }//GEN-LAST:event_indexTextFieldActionPerformed

    private void previousButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previousButtonActionPerformed
        // TODO add your handling code here:
        currentEntryIndex--;
        if(currentEntryIndex <0)
        currentEntryIndex = numberOfEntries -1;
        indexTextField.setText(""+(currentEntryIndex+1));
        indexTextFieldActionPerformed(evt);
    }//GEN-LAST:event_previousButtonActionPerformed

